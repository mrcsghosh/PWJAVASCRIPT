//The formula to calculate the area of a circle is π × radius^2.
// In this case, since the radius is given as 10, the program can be written as :

// Given radius value
let radius = 10;

// Calculate the area of the circle
let area = Math.PI * Math.pow(radius, 2);

// Display the result
console.log(`Radius: ${radius}`);
console.log(`Area of the Circle: ${area.toFixed(2)}`);
 /*
The Math.PI property is used to access the mathematical constant π.
Math.pow(radius, 2) is used to calculate the square of the radius.
The result is displayed in the console with two decimal places using toFixed(2).
*/

 