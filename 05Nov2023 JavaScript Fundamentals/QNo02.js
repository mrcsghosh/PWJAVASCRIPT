// Operators in JavaScript can be categorized based on their functionality.
// Here are the main categories of operators in JavaScript:

//1. Arithmetic Operators:

//Perform mathematical calculations on numeric values.

//Examples:
let a = 10;
let b = 5;
let sum = a + b;  // Addition: sum is 15
let difference = a - b;  // Subtraction: difference is 5
let product = a * b;  // Multiplication: product is 50
let quotient = a / b;  // Division: quotient is 2
let remainder = a % b;  // Modulus: remainder is 0

//2. Comparison Operators:

//Compare two values and return a Boolean result (true or false).

//Examples:
let x = 5;
let y = 10;
console.log(x === y);  // Strict equality: false
console.log(x !== y);  // Strict inequality: true
console.log(x > y);    // Greater than: false
console.log(x < y);    // Less than: true

//3. Logical Operators:

//Combine or manipulate Boolean values.

//Examples:
let isTrue = true;
let isFalse = false;
console.log(isTrue && isFalse);  // Logical AND: false
console.log(isTrue || isFalse);  // Logical OR: true
console.log(!isTrue);            // Logical NOT: false

//4. Assignment Operators:

//Assign values to variables.

//Examples:
let p = 5;
p += 3;  // Equivalent to x = x + 3
console.log(p);  // Output: 8

//5. Bitwise Operators:

//Perform operations at the bit level.

//Examples:
let e = 5;  // Binary: 0101
let f = 3;  // Binary: 0011
console.log(e & f);  // Bitwise AND: 1 (Binary: 0001)
console.log(e | f);  // Bitwise OR: 7 (Binary: 0111)


