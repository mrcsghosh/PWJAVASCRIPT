//use the while loop to print number from 1 to 25 in ascending and dscending order.

let num = 1;

//use while loop for ascending order
console.log("number from 1 to 25 in ascending order :");
while (num <= 25) {
    console.log(num);
    num++; 
}


//use while loop for dscending order
let Num = 25;
const minValueofNum = 1;
console.log("number from 1 to 25 in dscending order :");

while (Num >= minValueofNum) {
    console.log(Num);
    Num--;
}